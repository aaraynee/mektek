<p><b style="font-size: medium;">Why not a Support Forum?</b></p>
<p><span style="font-size: medium;">Simply put, so we can offer you superior support.</span></p>
<p><span style="font-size: medium;">A forum works only when you have a large enough active community -&nbsp;
thousands of people willing to participate on a daily basis. The smaller
the community, the more likely you are to wait for a long time before&nbsp;
someone answers your question. Unfortunately even the top authors in&nbsp;
Themeforest have only a few hundreds active users on a daily basis and&nbsp;
this is not enough to make the forum works</span></p>
